class Solution {

    public static void operations(int x, int y) {
        // Perform addition
        int p = x + y;

        // Perform subtraction
        int q = x - y;

        // Perform multiplication
        int r = x * y;

        // Perform division (integer division)
        int s = x / y;

        // Perform modulo
        int t = x % y;

        // Don't change this
        System.out.println(p + " " + q + " " + r + " " + s + " " + t);
    }
}